fun main(){
    var physics: Int = 80
    var english: Int = 70

    val sum = physics + english
    println(sum)

    val modulus = physics % english  //remainder
    println(modulus)

    val subtraction = physics - english
    println(subtraction)

    val division = physics / english
    println(division)

    val multiplication = physics * english
    println(multiplication)
}
